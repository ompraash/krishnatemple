                      <p>Krishna is the Supreme Person, the Godhead. Krishna is the speaker of the Bhagavad-gita, which is recognized throughout the world as one of mankind’s greatest books of wisdom. In the Gita, as it is also known, Krishna says repeatedly that He is God Himself, the source of everything. Arjuna, to whom Krishna is speaking, accepts Krishna’s words as true, adding that the greatest spiritual authorities of that time also confirm that Krishna is God.</p>

<p>As with anyone in our experience, God is unique and complex. He’s the transcendental Supreme Person, so there’s infinitely more to know about Him than anyone else. The Vedas, especially Srimad-Bhagavatam, supply detailed information about Him.</p>

<p>Everything about God is fully transcendental, or spiritual. Because God is absolute, there is no difference between Him and His name, form, activities, qualities, and so on. Contact with any of these gives the same spiritual benefit, namely purification of our consciousness.
Krishna's Form</p>

<p>The Vedas tell us that spirit is composed of eternity, knowledge (or consciousness), and happiness. Both God and we souls possess spiritual forms, which are free of the limitations of material form. For example, each part of a spiritual body can perform the function of any other part.</p>

                      <blockquote>The pure devotee is always within the core of My heart, and I am always in the heart of the pure devotee. My devotees do not know anything else but Me, and I do not know anyone else but them. —Srimad-Bhagavatam 9.4.68</blockquote>

                      <p>Krishna’s body never changes; He is an eternal youth.

<p>Unlike we ordinary souls, who may possess a material body, Krishna and His body are always identical.</p>
                        <p>The Vedas describe Krishna in this way: He is a beautiful youth with a glowing complexion the color of rain clouds. He plays a flute, attracting the hearts of all. His cheeks are brilliant, His smile enchanting. He wears a peacock feather in His curly black hair and a flower garland around His neck. His beautiful garments are the color of lightning. His toenails resemble the light of the moon.</p>

<p>Not only do the Vedas tell us what Krishna looks like, but pure souls have received His audience and written of their encounters. And fifty centuries ago, Krishna revealed His transcendental form to residents of India when He lived there for 120 years, sometimes showing and sometimes hiding His divinity.</p>
<blockquote>I envy no one, nor am I partial to anyone. I am equal to all. But whoever renders service unto Me in devotion is a friend, is in Me, and I am also a friend to him. —Sri Krishna, Bhagavad-gita 9.29</blockquote>
<p>
Krishna is loving by nature. In our original pure state, we love Him fully. In the exchange of complete love, Krishna considers His devotees greater than Himself. In the spiritual world He always stays with His devotees, and in this world He resides in every heart as the Supersoul. He wants His children in the material world to return to Him to enjoy with Him eternally.</p>

<p>Krishna is completely independent, and we cannot comprehend him completely. He cannot be conquered by knowledge. But He can be conquered—and seen directly—through pure love.</p>

<p><strong>God has feelings:</strong> He is satisfied when someone offers Him a nice prayer. Even though He is great, He can be moved by our love. He responds to us according to how we approach Him.</p>
