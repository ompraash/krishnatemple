                        <h2 class="widget__title">Vedas</h2>
                      <p>The Vedic scriptures are the spiritual literature of the ancient Indian culture, written in the Sanskrit language. They are comprised of a huge collection of books which include material (mundane), religious (ritualistic) as well as spiritual (monotheistic) knowledge. The expression "Vedic" is derived from the Sanskrit word VEDA, which means knowledge or revelation. According to the Vedic history, they were written down thousands of years ago. The date, however, is not very important because, without a doubt, the knowledge contained in these scriptures was existing a long time before it was written down.</p>

<p>The Veda may be understood by simply accepting what the Veda says about itself. Since the Vedic self-understanding may be amazing or even unbelievable to the modern reader, it seems important to dedicate a few sentences to the clarification of probable misunderstandings. The different opinions about the origin and history of the Vedic scriptures are due to the fundamental difference of world-views between the followers of the Veda and modern mundane scholars.</p>

<p><strong>The four Vedas:</strong> The scriptures of this category are generally labeled as the original Vedic scriptures. These four Vedas are known as <strong>Rig, Yajur, Sama and Atharva</strong>. Rig means ritual, and it contains mainly hymns and prayers (Mantras) in the worship of the universal forces called the demigods. Yajur means ceremony, and it mainly describes how to perform the rituals. Sama means singing; the scriptures of these categories contain many other mantras as well as strict rules how to chant these mantras according to mystic vibrations. Atharva means a priest who knows the secret lore; these scriptures describe many different kinds of worship and invocations. In a broader sense, many other scriptures of material knowledge are also counted in the Atharva, like the Ayurveda (pharmacological sciences and the means to maintain health.)</p>

<p>The Vedas tell us that spirit is composed of eternity, knowledge (or consciousness), and happiness. Both God and we souls possess spiritual forms, which are free of the limitations of material form. For example, each part of a spiritual body can perform the function of any other part.</p>

<h2 class="widget__title">Srimad Bhagavatam</h2>
                      <p>Srimad-Bhagavatam, an epic philosophical and literary classic, holds a prominent position in India’s voluminous written wisdom. The Bhagavatam is the postgraduate, post-Vedic text of spiritual understanding. It has served as the inspiration for countless works of literature, song, drama, painting, and sculpture.</p>

<p>Known as “the ripe fruit of the tree of Vedic literature,” Srimad-Bhagavatam is the most complete and authoritative exposition of Vedic knowledge. Covering everything from the nature of the self to the origin of the universe, it touches upon all fields of knowledge.</p>

<p><strong>Vyasadeva</strong>  compiled Srimad-Bhagavatam, after Krishna left this world 5,000 years ago. The 18,000-verse treatise centers on the science of God and devotion to Him, and includes biographies of great devotees who followed the path of Bhakti and attained Krishna.</p>

<p>The first verse of Srimad-Bhagavatam makes clear that the book is intended for people serious about spiritual progress; it will not deal with sectarian religious ideas, philosophical conjecture, or worldly concerns. The second text promises that anyone who reads the book systematically will achieve the spiritual success meant for all human beings.</p>

<h2 class="widget__title">Bhagavad Gita</h2>
                      <p>Krishna Himself came to this world 5,000 years ago and spoke Bhagavad-gita to His friend Arjuna.</p>

<p>At the last moment before entering a momentous battle, the great warrior Arjuna begins to wonder: "Why should I fight? What is the meaning of life? Where am I going after death?" In response, Krishna brings His friend from perplexity to spiritual enlightenment. The Gita contains detailed explanations of the supreme, infinite soul (God), the finite soul (the living entities), karma (work in this world), time, and creation, and lucidly explains the nature of consciousness and the universe. The essence of India’s spiritual wisdom, Bhagavad-gita is in a category of its own in philosophy and theology and has been pondered and deeply admired by philosophers and scholars around the world for centuries.</p>

<p>Though considered an introduction to the science of the soul, devotion, and God, the text is deep and sublime. The largest-selling edition of the Gita in the Western world, Bhagavad-gita As It Is is more than a book. It is alive with knowledge and devotion; thus it has the power to change your life for the better. </p>

<p>Bhagavad-gita is knowledge of five basic truths and the relationship of each truth to the other: These five truths are Krishna, or God, the individual soul, the material world, action in this world, and time. The Gita lucidly explains the nature of consciousness, the self, and the universe. It is the essence of India’s spiritual wisdom, the answers to questions posed by philosophers for centuries.</p>
