<p>One of the most awe-inspiring of Lord Krishna's forms is Sri Nrsimhadeva, His half-man, half-lion incarnation. Lord Nrsimha descended to protect His devotee Sri Prahlada Maharaja from the atheistic king Hiranyakashipu, Prahlada's father.</p>
