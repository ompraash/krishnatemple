                        <h2 class="widget__title">Sri Sri Radha and Krishna</h2>
                      <p>In the Vedas, Krishna reveals how the deity is to be made and worshiped. When Krishna’s form is created in this authorized way, He is personally present to accept service from His devotees.</p>

<blockquote>Krishna states in Bhagavad-gita (9.26), “If one offers Me with love and devotion a leaf, a flower, fruit or water, I will accept it.”</blockquote>

<p>The complete conception of God includes God and His energies, which exist simultaneously with Him and are in a sense identical to Him. The sum of Krishna’s energies is Radha, His eternal consort. Like the sun and sunshine, Krishna and Radha are one.</p>

<p>Radha embodies the totality of love for Krishna. Thus She is known as the personification of Bhakti, or loving devotion to God.</p>

<p>Radha-Krishna are the male and female aspects of God. Known as the Divine Couple, together they are the full manifestation of God.</p>

                      <blockquote>Radharani is a tenderhearted feminine counterpart of the supreme whole, resembling the perfectional stage of the worldly feminine nature. Therefore, the mercy of Radharani is available very readily to the sincere devotees, and once She recommends such a devotee to Lord Krishna, the Lord at once accepts the devotee’s admittance into His association. —Srila Prabhupada, Bhagavatam 2.3.23</blockquote>

 <h2 class="widget__title">Sita, Rama, Lakshman, and Hanuman</h2>
                      <p>Millions of years ago, in the age called Treta-yuga, the Supreme Personality of Godhead descended as a king, Lord Rama, or Ramachandra, to establish the principles of religion, morality and righteousness. His pastimes were recorded by the poet Valmiki in the epic Ramayana</p>

<blockquote>The great sage Sukadeva explains: "Lord Ramachandra's spotless name and fame, which vanquish all sinful reactions, are celebrated in all directions … all the saintly kings and all the demigods, including Lord Shiva and Lord Brahma, worship the Lord by bowing down with their helmets. Let me offer my obeisances unto His lotus feet."</blockquote>

<p>The supreme royal couple is even worshiped outside India. In Thailand, for example, a quarter-mile stretch of the halls of the royal palace is artistically painted with scenes from the Ramayana. On the island of Bali in Indonesia, and also in Cambodia and Nepal, thousands more Rama temples can be found. In every corner of India, from tiny village shrines to fabulous temple palaces like Hare Krishna Land at Juhu Beach, Mumbai, the transcendental form of Lord Rama is worshiped, His all-liberating name sung by His devotees.</p>

 <h2 class="widget__title">Lord Nrsimha: Protector of Devotees</h2>
